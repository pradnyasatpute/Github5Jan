package Controlflowstatements;

/**
 * Syntax:
 *    for(initialization;condition;incr/decr){
 *        //code
 *    }
 */
public class ForLoop {
    public static void main(String[] args) {
        for (int i=1;i<=10;i++){
            System.out.println(i);
        }
    }
}

